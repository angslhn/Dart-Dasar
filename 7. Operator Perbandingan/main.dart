// Operator Perbandingan

// Beberapa operator dibawah ini, dapat mengembalikan booolean antara true dan false.
// >  Lebih besar dari
// <  Lebih kurang dari
// >= Lebih besar dari atau sama dengan
// <= Kurang dari atau sama dengan
// == Sama dengan
// != Tidak sama dengan

void main() {
 int angka_pertama = 10;
 int angka_kedua = 5;

 print(angka_pertama == angka_kedua); 
 print(angka_pertama < angka_kedua);
 print(angka_pertama > angka_kedua);
 print(angka_pertama <= angka_kedua);
 print(angka_pertama >= angka_kedua);
}